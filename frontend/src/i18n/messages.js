export function getInitialLanguage() {
  const saved = localStorage.getItem('uaos-lang');
  if (saved && messages[saved]) return saved;

  const browser = navigator.language?.slice(0, 2);
  if (browser && messages[browser]) return browser;

  return 'ar';
}
