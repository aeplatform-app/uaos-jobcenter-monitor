function getLang(){
  const url = new URL(location.href);
  const q = url.searchParams.get("lang");
  if(q && UAOS_I18N[q]) return q;
  const saved = localStorage.getItem("uaos_lang");
  return UAOS_I18N[saved] ? saved : "en";
}

function applyLang(lang){
  const t = UAOS_I18N[lang] || UAOS_I18N.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = t.dir || "ltr";
  localStorage.setItem("uaos_lang", lang);
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    if(t[key]) el.textContent = t[key];
  });
  document.querySelectorAll("[data-lang]").forEach(b=>b.classList.toggle("active", b.getAttribute("data-lang")===lang));
}

function showLocked(message){
  const modal = document.getElementById("uaosModal");
  if(!modal) return alert(message || "Private beta only. Disabled for now.");
  const p = modal.querySelector("p");
  if(p && message) p.textContent = message;
  modal.classList.add("show");
}

function closeLocked(){
  document.getElementById("uaosModal")?.classList.remove("show");
}

function playDemoTone(){
  const AudioContext = window.AudioContext || window.webkitAudioContext;
  const ctx = new AudioContext();
  const master = ctx.createGain();
  master.gain.value = 0.18;
  master.connect(ctx.destination);
  const notes = [261.63,311.13,392.0,466.16,523.25,466.16,392.0,311.13,261.63];
  notes.forEach((f,i)=>{
    const o = ctx.createOscillator();
    const g = ctx.createGain();
    o.type = i % 2 ? "triangle" : "sine";
    o.frequency.value = f;
    g.gain.setValueAtTime(0.0001, ctx.currentTime + i*.20);
    g.gain.exponentialRampToValueAtTime(.7, ctx.currentTime + i*.20 + .03);
    g.gain.exponentialRampToValueAtTime(.0001, ctx.currentTime + i*.20 + .18);
    o.connect(g); g.connect(master);
    o.start(ctx.currentTime + i*.20);
    o.stop(ctx.currentTime + i*.20 + .2);
  });
}

function fixDeadButtons(){
  document.querySelectorAll("a.btn, button.btn").forEach(el=>{
    const text = (el.textContent || "").toLowerCase();
    const href = el.getAttribute("href");

    if(el.hasAttribute("data-tone")) return;
    if(el.hasAttribute("data-locked")) return;

    if(el.tagName.toLowerCase()==="a"){
      if(!href || href==="#" || href.trim()===""){
        el.setAttribute("href","contact.html");
        el.classList.add("beta");
        return;
      }

      if(text.includes("download") || text.includes("payment") || text.includes("buy") || text.includes("subscribe") || text.includes("checkout")){
        el.setAttribute("href","#");
        el.setAttribute("data-locked","true");
        el.classList.add("locked");
        return;
      }
    }

    if(el.tagName.toLowerCase()==="button" && !el.hasAttribute("onclick")){
      if(text.includes("play") || text.includes("demo") || text.includes("tone")){
        el.setAttribute("data-tone","true");
      } else {
        el.setAttribute("data-locked","true");
        el.classList.add("locked");
      }
    }
  });
}

document.addEventListener("DOMContentLoaded", ()=>{
  fixDeadButtons();

  document.querySelectorAll("[data-lang]").forEach(btn=>{
    btn.addEventListener("click",()=>applyLang(btn.getAttribute("data-lang")));
  });

  document.querySelectorAll("[data-locked]").forEach(el=>{
    el.addEventListener("click",e=>{
      e.preventDefault();
      showLocked("Private beta only. This action is intentionally disabled until owner approval.");
    });
  });

  document.querySelectorAll("[data-tone]").forEach(el=>{
    el.addEventListener("click",e=>{
      e.preventDefault();
      playDemoTone();
    });
  });

  applyLang(getLang());
});
